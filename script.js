const container = document.getElementById("cardsContainer");
const searchInput = document.getElementById("searchInput");
const clearBtn = document.getElementById("clearBtn");

function renderizarCaixas(lista) {
  if (!container) {
    console.error("❌ #cardsContainer não encontrado no HTML");
    return;
  }

  const modelosQueMostramTamanhos = ["Oitavada", "Smart", "Separada"];

  container.innerHTML = lista.map(caixa => {
    const agrupar = modelosQueMostramTamanhos.includes(caixa.modelo);

    // imagem principal do modelo (primeira que existir)
    const imagemModelo = caixa.tamanhos.find(t => t.url)?.url || "img/sem-imagem.png";

    // ====== AGRUPADO: 1 card só por modelo ======
    // ====== AGRUPADO: 1 card só por modelo (tamanhos horizontais) ======
    if (agrupar) {
      const tamanhosBadges = caixa.tamanhos.map(t => {
        const title = t.medidas ? `title="${t.medidas}"` : "";
        return `
      <span class="size-badge" ${title}>
        ${t.tamanho}${t.medidas ? ` <small>(${t.medidas})</small>` : ""}
      </span>
    `;
      }).join("");

      return `
    <div class="col-md-4 mb-4">
      <div class="card h-100 shadow-sm caixa-card" data-modelo="${caixa.modelo}">
        <div class="text-center p-3">
          <img 
            src="${imagemModelo}" 
            alt="${caixa.modelo}" 
            class="img-fluid" 
            style="max-height:180px; object-fit:contain;"
          >
        </div>

        <div class="card-body text-center">
          <h5 class="card-title">${caixa.modelo}</h5>

          <div class="card-text text-muted mt-2">
            <strong>Tamanhos:</strong>
            <div class="sizes-row mt-2">
              ${tamanhosBadges}
            </div>
          </div>
        </div>
      </div>
    </div>
  `;
    }

    // ====== NORMAL: 1 card por tamanho (TÍTULO = Modelo + Tamanho) ======
    return caixa.tamanhos.map(item => {
      const urlImg = item.url || "img/visu-indisponivel.png";

      return `
        <div class="col-md-4 mb-4">
          <div class="card h-100 shadow-sm caixa-card"
               data-modelo="${caixa.modelo}"
               data-tamanho="${item.tamanho}"
               data-medidas="${item.medidas || ""}"
               data-url="${urlImg}">

            <div class="text-center p-3">
              <img 
                src="${urlImg}" 
                alt="${caixa.modelo} ${item.tamanho}" 
                class="img-fluid" 
                style="max-height:180px; object-fit:contain;"
              >
            </div>

            <div class="card-body text-center">
              <h5 class="card-title">${caixa.modelo} ${item.tamanho}</h5>
              <p class="card-text text-muted">
                <strong>Medidas:</strong> ${item.medidas || "Indisponível"}
              </p>
            </div>

          </div>
        </div>
      `;
    }).join("");

  }).join("");

  // Clique nos cards (delegação)
  container.querySelectorAll(".caixa-card").forEach(card => {
    card.addEventListener("click", () => {
      const modelo = card.dataset.modelo;
      const tamanho = card.dataset.tamanho;
      const medidas = card.dataset.medidas;
      const url = card.dataset.url;

      // se não tem tamanho => card agrupado
      if (!tamanho) {
        const caixa = lista.find(c => c.modelo === modelo);
        openModalModelo(caixa);
      } else {
        openModalTamanho({ modelo, tamanho, medidas, url });
      }
    });
  });
}

