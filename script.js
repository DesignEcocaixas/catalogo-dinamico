// Função para gerar o PDF
downloadBtn.addEventListener("click", () => {
  // Esconde os botões temporariamente
  searchInput.style.display = "none";
  clearBtn.style.display = "none";
  downloadBtn.style.display = "none";

  // Captura o body
  html2canvas(document.body, { scale: 1 }).then(canvas => {
    // Reduz qualidade da imagem (compressão)
    const imgData = canvas.toDataURL("image/jpeg", 0.7); // 0.7 = 70% de qualidade

    const { jsPDF } = window.jspdf;
    const pdf = new jsPDF("p", "mm", "a4");

    const pageWidth = pdf.internal.pageSize.getWidth();
    const pageHeight = pdf.internal.pageSize.getHeight();

    const imgProps = pdf.getImageProperties(imgData);
    const pdfHeight = (imgProps.height * pageWidth) / imgProps.width;

    let heightLeft = pdfHeight;
    let position = 0;

    pdf.addImage(imgData, "JPEG", 0, position, pageWidth, pdfHeight);
    heightLeft -= pageHeight;

    while (heightLeft > 0) {
      position = heightLeft - pdfHeight;
      pdf.addPage();
      pdf.addImage(imgData, "JPEG", 0, position, pageWidth, pdfHeight);
      heightLeft -= pageHeight;
    }

    pdf.save("Catalogo_Completo.pdf");

    // Mostra os botões novamente
    searchInput.style.display = "inline-block";
    clearBtn.style.display = "inline-block";
    downloadBtn.style.display = "inline-block";
  });
});

// --- Criar estrutura do modal no HTML via JS ---
const modal = document.createElement("div");
modal.id = "modal";
modal.style.display = "none";
modal.innerHTML = `
  <div class="modal-content">
    <button id="closeModal" type="button" aria-label="Fechar modal">✕</button>
    <img id="modalImg" src="" alt="">
    <h3 id="modalNome"></h3>

    <div id="modalSizes" class="modal-sizes mt-2"></div>

    <p id="modalMedida" class="mt-2" style="display:none;"></p>
  </div>
`;
document.body.appendChild(modal);

function abrirModal() {
  modal.style.display = "flex";
  // força reflow pra transição pegar sempre
  void modal.offsetWidth;
  modal.classList.add("modal-open");
}

function fecharModal() {
  modal.classList.remove("modal-open");
  // espera a animação terminar antes de esconder
  setTimeout(() => {
    modal.style.display = "none";
  }, 220);
}

function openModalTamanho(item) {
  document.getElementById("modalImg").src = item.url || "img/sem-imagem.png";
  document.getElementById("modalNome").textContent = `${item.modelo} ${item.tamanho}`;

  const modalSizes = document.getElementById("modalSizes");
  const modalMedida = document.getElementById("modalMedida");

  // esconde badges e mostra texto de medida
  modalSizes.style.display = "none";
  modalMedida.style.display = "block";
  modalMedida.textContent = item.medidas || "Medida não informada";

  abrirModal();
}

function openModalModelo(caixa) {
  const imagemModelo = caixa.tamanhos.find(t => t.url)?.url || "img/sem-imagem.png";

  document.getElementById("modalImg").src = imagemModelo;
  document.getElementById("modalNome").textContent = caixa.modelo;

  const modalSizes = document.getElementById("modalSizes");
  const modalMedida = document.getElementById("modalMedida");

  // mostra grades/badges e esconde texto
  modalMedida.style.display = "none";
  modalSizes.style.display = "flex";

  modalSizes.innerHTML = (caixa.tamanhos || []).map(t => {
    const title = t.medidas ? `title="${t.medidas}"` : "";
    return `
      <span class="size-badge" ${title}>
        ${t.tamanho}${t.medidas ? ` <small>(${t.medidas})</small>` : ""}
      </span>
    `;
  }).join("") || `<span class="size-badge">Sem tamanhos cadastrados</span>`;

  abrirModal();
}

// --- Fechar modal ---
document.getElementById("closeModal").addEventListener("click", fecharModal);

// Fecha ao clicar fora do conteúdo
window.addEventListener("click", (e) => {
  if (e.target === modal) fecharModal();
});

// (opcional) fechar com ESC
window.addEventListener("keydown", (e) => {
  if (e.key === "Escape" && modal.style.display === "flex") fecharModal();
});

